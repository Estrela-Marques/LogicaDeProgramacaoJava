
public class TiposNumericos {
	
	public static void main(String[] args) {
		
		Byte numeroByte = 127; // -128 até 127 ()Intervalo de números que posso utilizar no tipo Byte.
		
		Short numeroShort = -32768; // -32768 até 32767
		
		Integer numeroInteger = 2147483647; // -2147483648 até 2147483647
		
		Long numeroLong = 1L; // - 9.223.372.037x10 até 9.223.372.037x10
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
}
}
	
