
public class TiposPrimitivos {
	
	public static void main(String[] args) {
		String variavelString = ""; // NÃ£o tem primitivo dessa linguagem.
		Byte variavelByte = 1; // primitivo (byte). 
		
		Short variavelShort = 1; // primitivo (short)
		
		Integer variavelInteger = 1; // primitivo (int)
		
		Long variavelLong = 1L; // primitivo (long)
		
		Float variavelFloat = 1.f; // primitivo (float)
		
		Double variavelDouble = 1.0; // primitivo (double)
		
		Character variavelCharacter = 'A'; // primitivo (char)
		
		Boolean variavelBoolean = true; // primitivo (boolean)
		
		
		
	}

}
